        $eventos = DB::table('coord_comissao_organizadoras')
        ->join('eventos','coord_comissao_organizadoras.eventos_id','=','eventos.id')
        ->select('eventos.*')
        ->where('coord_comissao_organizadoras.user_id',$user->id)
        ->get();

